# -- Strings --
# strings are used to store characters
name = "Aziz"
name = "Habibi"

print(name)
print(name * 2) #output: HabibiHabibi

# -- Changing variables --
# Variables are names for values.

a = 25
b = a

# Here we've given the value '25' the names 'a' and 'b'.

print(a)
print(b)

b = 17

# Here we've given the value '17' the name 'b'. The name 'a' is still a name for '25'!

print(a) #output: 25
print(b) #output: 17